var oMore = document.getElementById('more');
var oBd_more = document.getElementById('bd_more');
oMore.onmouseover = function(){
	oBd_more.style.display = 'block';
}
oBd_more.onmouseover = function(){
	oBd_more.style.display = 'block';
}
oBd_more.onmouseout = function(){
	oBd_more.style.display = 'none';
}
